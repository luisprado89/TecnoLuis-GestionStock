    // Método para iniciar la carga y el login
    private void iniciarCargaYLogin(Usuario usuario) {
        jProgressBar1Login.setVisible(true);
        jProgressBar1Login.setValue(0);

        Timer timer = new Timer(50, null); // cada 50 ms
        timer.addActionListener(new ActionListener() {
            int progreso = 0;

            @Override
            public void actionPerformed(ActionEvent e) {
                progreso += 5;
                jProgressBar1Login.setValue(progreso);

                if (progreso >= 100) {
                    timer.stop();
                    // Abrir la ventana principal
                    Sistema sistema = new Sistema(usuario.getNombre(), usuario.getRol());
                    sistema.setVisible(true);
                    dispose(); // Cierra la ventana Login
                }
            }
        });

        timer.start(); // Inicia la animación
    }



