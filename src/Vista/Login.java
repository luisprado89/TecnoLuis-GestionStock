    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
        //Datos de inicio de sesión correo y contraseña que introduce el usuario
        String correo = txtCorreoLogin.getText().trim();
        String pass = new String(jPasswordLogin.getPassword()).trim();

        //Comprobamos que los campos no estén vacíos
        if (correo.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete ambos campos.");
            return;
        }
        LoginDao loginDao = new LoginDao();
        Usuario usuario = loginDao.login(correo, pass);

        if (usuario != null) {
            JOptionPane.showMessageDialog(this, "Bienvenido, " + usuario.getNombre() + " (" + usuario.getRol() + ")");
            // Aquí puedes abrir la ventana principal del sistema y cerrar esta
            //  Abrir la ventana principal con nombre y rol
//            Sistema sistema = new Sistema(usuario.getNombre(), usuario.getRol());
//            sistema.setVisible(true);
//            this.dispose(); // Cierra la ventana de login
            iniciarCargaYLogin(usuario);

        } else {
            JOptionPane.showMessageDialog(this, "Correo o contraseña incorrectos.");
        }
    }//GEN-LAST:event_btnAccederActionPerformed

